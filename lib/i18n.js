const DICTS = { es, en };

export function translate(lang, key) {
  const dict = DICTS[lang] || DICTS[DEFAULT_LANG];
  if (Object.prototype.hasOwnProperty.call(dict, key)) return dict[key];
  // fallback to spanish
  if (Object.prototype.hasOwnProperty.call(es, key)) return es[key];
  return key;
}
